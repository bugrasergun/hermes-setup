def main():
    parser = argparse.ArgumentParser(
        description="Brain Semantic Search — Index & Search CLI (Katman 2+3)"
    )
    group = parser.add_mutually_exclusive_group(required=True)
    group.add_argument("--rebuild", action="store_true",
                       help="Full re-index (drops everything, re-inserts all)")
    group.add_argument("--update", action="store_true",
                       help="Only index changed files (hash-based)")
    group.add_argument("--status", action="store_true",
                       help="Show index statistics")
    group.add_argument("--search", type=str, metavar="QUERY",
                       help="Semantic search query")

    parser.add_argument("--top", type=int, default=5,
                        help="Number of results to return (default: 5)")
    parser.add_argument("--show-content", action="store_true",
                        help="Print chunk content in search results")
    parser.add_argument("--profile", type=str, default="shared",
                        help="Agent profile for search: 'shared' (default), "
                             "agent name (e.g. 'maya'), or 'all'")

    args = parser.parse_args()
    conn = get_conn()
    init_table(conn)

    try:
        if args.rebuild:
            rebuild_index(conn)
        elif args.update:
            update_index(conn)
        elif args.status:
            show_status(conn)
        elif args.search:
            results = search_brain(conn, args.search, args.top,
                                   args.show_content, args.profile)
            format_search_results(results, args.search, args.show_content)
    finally:
        conn.close()


if __name__ == "__main__":
    main()
