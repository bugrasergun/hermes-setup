#!/usr/bin/env python3
"""
Brain Semantic Search — Index & Search CLI (Katman 2 + 3)
============================================================
Indexes ~/brain/ files into PostgreSQL (pgvector) for semantic search.
Supports: markdown (.md), PDF (.pdf), text (.txt), transcripts (.srt/.vtt).
Uses Ollama nomic-embed-text (768-dim) + PyMuPDF4LLM for PDF extraction.
Each chunk is tagged with agent_profile for per-agent isolation.

Usage:
    python index_brain.py --rebuild                 # Full re-index all files
    python index_brain.py --update                  # Only index changed files (hash-based)
    python index_brain.py --status                  # Show index statistics
    python index_brain.py --search "query"          # Semantic search (shared only)
    python index_brain.py --search "query" --profile maya     # Search maya + shared
    python index_brain.py --search "query" --profile all      # Search everything
    python index_brain.py --search "query" --top 10 --show-content

Requirements (all in Honcho venv):
    psycopg (v3), httpx, pymupdf4llm, pymupdf
    ~/honcho/.venv/bin/python
"""

